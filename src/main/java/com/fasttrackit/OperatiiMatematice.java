package com.fasttrackit;

public class OperatiiMatematice {

    public double adunare(double nr1, double nr2) {
        double sum = nr1 + nr2;
        return sum;
    }

    public int adunare(int nr1, int nr2) {
        int sum = nr1 + nr2;
        return sum;
    }

    public double scadere(double nr1, double nr2) {
        return (nr1 - nr2);
    }

    public int scadere(int nr1, int nr2) {
        int dif = nr1 - nr2;
        return dif;
    }

    public double inmultire(double nr1, double nr2) {
        double inm = nr1 * nr2;
        return inm;
    }

    public int inmultire(int nr1, int nr2) {
        int inm = nr1 * nr2;
        return inm;
    }

    public double impartire(double nr1, double nr2) {
        double imp = nr1 / nr2;
        return imp;
    }

    public int impartire(int nr1, int nr2) {
        int imp = nr1 / nr2;
        return imp;
    }
}
